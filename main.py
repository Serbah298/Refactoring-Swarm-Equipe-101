    print(f"{'='*70}\n")

    # Codes de sortie pour le Bot de Correction
    if files_failed == 0:
        sys.exit(0)  # Succ√®s total
    elif files_passed > 0:
        sys.exit(0)  # Succ√®s partiel (acceptable)
    else:
        sys.exit(1)  # √âchec total


