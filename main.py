                print(f"\nüîÅ It√©ration {iteration+1}/3 pour {filename}")
                
                # Tester le fichier corrig√©
                success, feedback = judge.run_tests(os.path.dirname(fixed_path))

                if success:
                    print(f"‚úÖ {filename} valid√© !")
                    files_passed += 1
                    break
